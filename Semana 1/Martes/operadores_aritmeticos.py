n_uno = 10
n_dos = 2

# Suma (+)
suma = n_uno + n_dos
print(f'La suma de {n_uno} y {n_dos} es igual a : {suma}')

# Resta (-)
resta = n_uno - n_dos
print(f'La resta de {n_uno} y {n_dos} es igual a : {resta}')

# Multiplicación (*)
multiplicacion = n_uno * n_dos
print(f'La multiplicación de {n_uno} y {n_dos} es igual a : {multiplicacion}')

# División (/)
division = n_uno / n_dos
print(f'La división de {n_uno} y {n_dos} es igual a : {division}')

# División Exacta (//)
division_exacta = n_uno // n_dos
print(
    f'La división exacta de {n_uno} y {n_dos} '
    f'es igual a : {division_exacta}'
)

# Residuo | Modulo (%)
residuo = n_uno % n_dos
print(f'El residuo de {n_uno} y {n_dos} es igual a : {residuo}')

# Potenciación (**)
potencia = n_uno ** n_dos
print(f'La potencia de {n_uno} elevado a {n_dos} es igual a : {potencia}')
